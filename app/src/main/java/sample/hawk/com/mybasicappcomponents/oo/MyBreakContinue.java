public class MyBreakContinue {

    void test_break(){
        SMLog.i("test_break ++++++++++++++++");
        for(int i=1; i<10; i++){
            SMLog.i("  i = " + i);
            for(int j=1; j<10; j++) {
                SMLog.i("    j = " + j);
                if(i == j)
                {
                    SMLog.i("      (i==j) =" + i);
                    break;
                }
                // SMLog.i("break will bypass this debugmsg ");
            }
            SMLog.i("."); // break will show this debugmsg
        }
        SMLog.i("test_break ----------------");
    }

    void test_continue(){
        SMLog.i("test_continue ++++++++++++++++");
        for(int i=1; i<10; i++){
            SMLog.i("  i = " + i);
            for(int j=1; j<10; j++) {
                SMLog.i("    j = " + j);
                if(i == j)
                {
                    SMLog.i("      (i==j) =" + i);
                    continue;
                }
                // SMLog.i("continue will bypass this debugmsg ");
            }
            SMLog.i("."); // break will bypass this debugmsg too.
        }
        SMLog.i("test_continue ----------------");
    }

    void test_break_continue(){
        SMLog.i("test_break_continue ++++++++++++++++");
        for(int i=1; i<10; i++){
            SMLog.i("  i = " + i);
            for(int j=1; j<10; j++) {
                SMLog.i("    j = " + j);
                if(i == j)
                {
                    SMLog.i("      (i==j) =" + i);
                    break;
                }
                // SMLog.i("break will Bypass MSG1 ");
                if((i%j)==2)
                {
                    SMLog.i("      (i%j)==2   i="+ i+"  j="+j);
                    continue;
                }
                // SMLog.i("continue will Bypass MSG2 ");
            }
            SMLog.i(".");
        }
        SMLog.i("test_break_continue ----------------");
    }

}
